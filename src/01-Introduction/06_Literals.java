class Literals {
	public static void main( String[] args ) {
		byte b1 = 10; // Decimal Number
		byte b2 = 0b1010; // Binary Number
		byte b3 = 012; // Octal Number
		byte b4 = 0xA; // Hexadecimal Number

		System.out.println( b1 );

		System.out.println( b2 );

		System.out.println( b3 );

		System.out.println( b4 );
	}
}
